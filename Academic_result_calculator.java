      public void total()
      {
             Subject1=Double.parseDouble(Input1.getText());
             Subject2=Double.parseDouble(Input2.getText());
             Subject3=Double.parseDouble(Input3.getText());
             Subject4=Double.parseDouble(Input4.getText());
             totalvalue=Subject1+Subject2+Subject3+Subject4;
             formal.format(totalvalue);
             average=totalvalue/4;
            String n1=Double.toString(totalvalue);
          Display1.setText("Total mark is: "+n1);
          String n2=Double.toString(average);
          Display2.setText("Avegrage Mark is: "+n2);
          GradBox=Grad();
          CGPA=CGPAMathod();
          Display3.setText("CGPA is: "+CGPA+" Grad is: "+GradBox);
           
          
      }
      public String CGPAMathod()
      {
          if (average>=80)
        {
            return "4.00";
        }
        else if (average>=75)
        {
            return "3.75";

        }
        else if(average>=70)
        {
            return "3.50";
        }
        else if(average>=65)
        {
            return "3.25";
        }
        else if(average>=60)
        {
            return "3.00";
        }
        else if(average>=55)
        {
            return "2.75";
        }
        else if(average>=50)
        {
            return "2.50";
        }
        else if(average>=45)
        {
            return "2.25";
        }else
        {
            return "0.00";
        } 
      }
              
      public String Grad()
      {
          if (average>=80)
        {
            return "A+";
        }
        else if (average>=75)
        {
            return "A";

        }
        else if(average>=70)
        {
            return "A-";
        }
        else if(average>=65)
        {
            return "B+";
        }
        else if(average>=60)
        {
            return "B";
        }
        else if(average>=55)
        {
            return "B-";
        }
        else if(average>=50)
        {
            return "C+";
        }
        else if(average>=45)
        {
            return "C";
        }else
        {
            return "F";
        }
          
      }
